package JavaSessions;

public class LocalVar {
	static int p; //class var
	static String m;
	static double d;

	public static void main(String[] args) {
		
		//local var
		int i = 10;
		int p = 20;
		System.out.println(i);
		
		System.out.println(p);
		System.out.println(m);
		System.out.println(d);

	}

}
